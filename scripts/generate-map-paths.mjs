#!/usr/bin/env node
// Reads public/brasil-states.geojson, projects with d3-geo, writes
// src/data/brasil-paths.ts with static SVG paths and centroids per state.
//
// Run: node scripts/generate-map-paths.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { geoMercator, geoPath, geoCentroid } from "d3-geo";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "..");

const VIEWBOX_W = 620;
const VIEWBOX_H = 720;

const geojsonPath = path.join(ROOT, "public/brasil-states.geojson");
const outPath = path.join(ROOT, "src/data/brasil-paths.ts");

const geojson = JSON.parse(fs.readFileSync(geojsonPath, "utf8"));

// Douglas-Peucker line simplification
function pointLineDistance(point, lineStart, lineEnd) {
  const [x, y] = point;
  const [x1, y1] = lineStart;
  const [x2, y2] = lineEnd;
  const dx = x2 - x1;
  const dy = y2 - y1;
  if (dx === 0 && dy === 0) {
    return Math.sqrt((x - x1) ** 2 + (y - y1) ** 2);
  }
  const t = ((x - x1) * dx + (y - y1) * dy) / (dx * dx + dy * dy);
  const tClamped = Math.max(0, Math.min(1, t));
  const px = x1 + tClamped * dx;
  const py = y1 + tClamped * dy;
  return Math.sqrt((x - px) ** 2 + (y - py) ** 2);
}

function douglasPeucker(points, tolerance) {
  if (points.length < 3) return points;
  let maxDist = 0;
  let maxIdx = 0;
  for (let i = 1; i < points.length - 1; i++) {
    const dist = pointLineDistance(points[i], points[0], points[points.length - 1]);
    if (dist > maxDist) {
      maxDist = dist;
      maxIdx = i;
    }
  }
  if (maxDist > tolerance) {
    const left = douglasPeucker(points.slice(0, maxIdx + 1), tolerance);
    const right = douglasPeucker(points.slice(maxIdx), tolerance);
    return left.slice(0, -1).concat(right);
  }
  return [points[0], points[points.length - 1]];
}

// Simplify each ring in each polygon of every feature.
// Tolerance is in lon/lat degrees — 0.01 ≈ 1.1km. Visible at our render size.
const SIMPLIFY_TOLERANCE = 0.025;

function simplifyFeature(feature) {
  const { geometry } = feature;
  if (geometry.type === "Polygon") {
    geometry.coordinates = geometry.coordinates.map((ring) =>
      douglasPeucker(ring, SIMPLIFY_TOLERANCE)
    );
  } else if (geometry.type === "MultiPolygon") {
    geometry.coordinates = geometry.coordinates.map((polygon) =>
      polygon.map((ring) => douglasPeucker(ring, SIMPLIFY_TOLERANCE))
    );
  }
  return feature;
}

geojson.features = geojson.features.map(simplifyFeature);

// Fit Brazil into the viewBox
const projection = geoMercator().fitSize([VIEWBOX_W, VIEWBOX_H], geojson);
const pathGen = geoPath(projection);

// Reduce float precision in the path d-string from ~13 decimals to 1.
// Brings the file from ~1.3 MB down to ~80 KB without visible quality loss
// at the resolutions we render at.
function compressPath(d) {
  // Round to integer pixels (viewBox is 620x720, so subpixel precision is wasted).
  let compressed = d.replace(/-?\d+\.\d+/g, (match) => {
    return Math.round(parseFloat(match)).toString();
  });
  // Collapse consecutive identical points: "L100,200L100,200" → "L100,200"
  compressed = compressed.replace(/([ML])(-?\d+),(-?\d+)\1\2,\3/g, "$1$2,$3");
  return compressed;
}

const states = geojson.features
  .map((feature) => {
    const sigla = feature.properties.sigla;
    const nome = feature.properties.name;
    const d = compressPath(pathGen(feature));
    const centroid = projection(geoCentroid(feature));
    return {
      sigla,
      nome,
      d,
      cx: Math.round(centroid[0] * 10) / 10,
      cy: Math.round(centroid[1] * 10) / 10,
    };
  })
  .sort((a, b) => a.sigla.localeCompare(b.sigla));

const out = `// Auto-generated by scripts/generate-map-paths.mjs — do not edit by hand.
// Source: public/brasil-states.geojson (codeforgermany/click_that_hood).

export interface EstadoPath {
  sigla: string;
  nome: string;
  d: string;
  cx: number;
  cy: number;
}

export const MAP_VIEWBOX = { width: ${VIEWBOX_W}, height: ${VIEWBOX_H} };

export const estadosPaths: EstadoPath[] = ${JSON.stringify(states, null, 2)};
`;

fs.writeFileSync(outPath, out);
console.log(`Wrote ${states.length} states to ${path.relative(ROOT, outPath)}`);
console.log(`Output size: ${(fs.statSync(outPath).size / 1024).toFixed(1)} KB`);
